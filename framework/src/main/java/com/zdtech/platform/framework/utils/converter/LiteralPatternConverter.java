package com.zdtech.platform.framework.utils.converter;


import com.zdtech.platform.framework.utils.Bundle;

/**
 * 普通文本转换器：直接将文本输出
 * @author qfxu
 *
 */
public class LiteralPatternConverter extends PatternConverter {

	private String literal;

	public LiteralPatternConverter(String value) {
		literal = value;
	}

	@Override
	protected String convert(Bundle bundle) {
		return literal;
	}

}
